#include "fractol.h"

void	info(t_env *e)
{
	int		x;
	int		y;

	y = 0;
	while (y < WIN_H)
	{
		x = WIN_W;
		while (x < WIN_W_M)
		{
			ft_put_pixels(e, x, y, e->tab_c[e->theme]);
			x++;
		}
		y++;
	}
}
