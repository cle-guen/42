#include "fractol.h"

int		mouse_funct(t_env *e)
{
	if (e->mouse[BUT1_KEY] == 1 && e->zoom < 125)
		zoom_in(e);
	if (e->mouse[BUT2_KEY] == 1 && e->zoom > -5)
		zoom_out(e);
	return (0);
}
