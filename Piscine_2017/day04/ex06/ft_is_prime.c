int		ft_is_prime(int nb)
{
	int i;
	int prime;

	prime = 0;
	i = 1;
	while (nb >= i)
	{
		if (nb % i == 0)
			prime = prime + 1;
		i++;
	}
	if (prime == 2)
		return (1);
	else
		return (0);
}
