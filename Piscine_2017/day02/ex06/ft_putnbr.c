void	ft_putchar(char c);

void	ft_putnbr(int nb)
{
	long l;

	l = nb;
	if (l < 0)
	{
		l = l * (-1);
		ft_putchar('-');
	}
	if (l >= 0 && l <= 9)
	{
		ft_putchar(l + '0');
	}
	else
	{
		ft_putnbr(l / 10);
		ft_putchar(l % 10 + '0');
	}
}
