int		ft_atoi(char *str)
{
	int i;
	int out;
	int n;

	i = 0;
	out = 0;
	n = 0;
	while (str[i] == 32 || (str[i] >= 9 && str[i] <= 13))
		i++;
	n = i;
	if (str[i] == '+' || str[i] == '-')
		i++;
	while (str[i] >= '0' && str[i] <= '9')
	{
		out = out * 10 + str[i] - '0';
		i++;
	}
	if (str[n] == '-')
		out = out * -1;
	return (out);
}
