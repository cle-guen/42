char	*ft_strcat(char *dest, char *src)
{
	int i;
	int gg;

	i = 0;
	gg = 0;
	while (dest[i] != '\0')
	{
		i++;
	}
	while (src[gg] != '\0')
	{
		dest[i] = src[gg];
		i++;
		gg++;
	}
	dest[i] = '\0';
	return (dest);
}
